/**
 * Definition for singly-linked list.
 * function ListNode(val, next) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.next = (next===undefined ? null : next)
 * }
 */

function ListNode(val, next) {
  this.val = (val === undefined ? 0 : val);
  this.next = (next === undefined ? null : next);
}

/**
 * @param {ListNode} l1
 * @param {ListNode} l2
 * @return {ListNode}
 */
var mergeTwoLists = function (l1, l2) {
  let cur = new ListNode();

  // 将新链表的头保存下来
  const head = cur;

  while (!!l1 && !!l2) {
    if (l1.val <= l2.val) {
      // 将next指针指向下一个 listNode
      cur.next = l1;
      l1 = l1.next;
    } else {
      cur.next = l2;
      l2 = l2.next;
    }
    cur = cur.next;
  }
  if (!l1) cur.next = l2;
  if (!l2) cur.next = l1;
  return head.next;


};
